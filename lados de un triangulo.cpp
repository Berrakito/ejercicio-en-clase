#include <stdio.h>

int main() {
	int dividendo, divisor, cociente = 0, resto;
	printf("Introduce el dividendo: ");
	scanf("%d", &dividendo);
	printf("Introduce el divisor: ");
	scanf("%d", &divisor);
	resto = dividendo;
	while (resto >= divisor) {
		resto -= divisor;
		cociente++;
	}
	printf("Cociente: %d\nResto: %d", cociente, resto);
	return 0;
}

